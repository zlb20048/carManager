package com.cars.simple.mode;

import android.os.Bundle;

/**
 * 
 * @author shifeng
 * 
 */
@SuppressWarnings("rawtypes")
public class ActivityRebackInfo {

	/**
	 * 记录返回操作跳转的activity
	 */
	public Class backClass = null;

	/**
	 * 返回操作时返回前一页面的缓存的bundle 参数
	 */
	public Bundle backClassBundle = null;

	/**
	 * 临时增加个变量用于控制页面跳转不销毁时返回不重新start activity
	 */
	public boolean backRefresh = false;

	public Class getBackClass() {
		return backClass;
	}

	public void setBackClass(Class backClass) {
		this.backClass = backClass;
	}

	public Bundle getBackClassBundle() {
		return backClassBundle;
	}

	public void setBackClassBundle(Bundle backClassBundle) {
		this.backClassBundle = backClassBundle;
	}
}
